#13. Створіть масив з 15 цілочисельних елементів і визначте серед них мінімальне значення.
import numpy as np #імпортуємо numpy
import random #імпортуємо random

y = np.array([random.randint(-15, 30) for i in range(15)]) #масив з рандомних цілих чисел
print(y)

q = min(y) #мінімальний елемент масиву
print(q)
